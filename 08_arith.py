# Operadores Aritmeticos

# Basicos
print(10 + 10)
print(10 - 5)
print(10 * 2)
print(10 / 2)

# Otros
# - Modulo
print(7 % 3) # 1
print(10 % 2) # 0

# - División entera
print(7 // 3) # 2
print(10 // 2) # 5

# - Exponenciación
print(2 ** 3) # 8
print(5 ** 2) # 25

'''
PEMDAS - Orden de priorización de los operadores
P - Parentesis
E - Exponentes
M - Multiplicación
D - División
A - Adición (+)
S - Sustracción (-)
'''
print(2 ** 3 + 3 - 7 / 1 // 4)# 10
print(2 ** 3) # 8
print(7 / 1 // 4) # 1.0
print(8 + 3 - 1) # 10

# No dividir entre 0
# print(10 / 0)

# Operadores con strings
print('Hola' + 'Mundo')
print('Hola' * 3)
